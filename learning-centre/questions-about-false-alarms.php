					<div class="blog-page-grid"><br>
						<h3><a href="#" style="color:#C93D3D;">What is a false alarm?</a></h3><br>
						<p>A false alarm is an alarm that triggers an emergency response when no emergency has occurred.</p>
                        <br>
                        <h3><a href="#" style="color:#C93D3D;">Why are false alarms a problem?</a></h3><br>
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>False alarms reduce the effectiveness of security systems. If alarms are seen as unreliable indicators of threats, they may potentially be ignored.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>False alarms exhaust the resources of police, fire and emergency personnel and keep them from responding to actual emergencies.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>False alarms increase costs for taxpayers, security service providers and users, requiring additional personnel to respond to alarms.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>False alarms can carry fines. In many cities and towns, the owners of systems that trigger false alarms can be fined for each occurrence.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>False alarms can lead to denial of service. In some cities and towns, police, fire and emergency personnel are instructed to ignore the alarms of users whose systems have set off false alarms in the past.</a></li>
                        </ul>
                        
                         <br>
                        <h3><a href="#" style="color:#C93D3D;">How can you help decrease the false alarm rate?</a></h3><br>
						
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Train the necessary people in the use of your home's alarm system and schedule annual review sessions.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Review your alarm system operating manual. Take time to educate everyone with access to your home on proper system operation. For copies of ADT manuals, call 1-800-ADT-ASAP.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Be familiar with ADT's procedures should we receive an alarm signal from your home, including how to cancel an accidental alarm. In some instances, ADT has begun verifying alarms and will attempt to contact you before summoning the appropriate authorities.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>If your system includes a back-up battery for use during an electrical power loss, check to make sure that it is fully charged. Low battery voltage is the second most common cause of accidental alarms. Most alarm systems visually indicate a problem with the battery.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>False alarms can lead to denial of service. In some cities and towns, police, fire and emergency personnel are instructed to ignore the alarms of users whose systems have set off false alarms in the past.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Regularly check alarm contact switches/magnets on doors and windows for damage or looseness. Call ADT if service adjustments are needed.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>If your system has glass-break detectors, call ADT to verify that they have been adjusted properly so that loud noises such as thunder or nearby construction will not set off the alarm.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Check that all doors and windows are locked and cannot be inadvertently opened by a strong breeze.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Motion detectors should be aimed away from air vents, sources of heat, fireplaces and windows. If this is not the case, call ADT for a service visit. Plants and other objects should also be placed out of the field of vision.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Regularly check local alarm ordinances, if any, that govern the operation of the system and ensure that all permits, fees and other required components are in compliance to prevent any pitfalls with local authorities.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Periodically test your alarm system with the ADT monitoring station as recommended in your owner's manual to ensure that signals are being properly received. Always call ADT before you test your system.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Always advise ADT before any interior remodeling (including air vent repair, extensive painting, window replacement and any changes to the location of doors, walls or windows) that may affect the alarm system configuration.</a></li>
                           
                        </ul>
                        
                         <br>
                        <h3><a href="#" style="color:#C93D3D;">What will happen if your system emits a false alarm?</a></h3><br>
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Users with recurring false alarms may be subject to municipal fines. Fines for false alarms vary depending on your residence location.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Users with recurring false alarms can be subject to suspension or cancellation of services. The service penalty imposed by police, fire or emergency dispatch units varies depending on municipality.</a></li>
                           
                        </ul>
                        
