						<h3><a href="#" style="color:#C93D3D;">Home Security Glossary</a></h3><br>
						<p>When it comes to discussing home security terminology, sometimes the language can be confusing. We've designed this page to help you get answers to your security questions and to help explain what certain security and safety terms mean. The better informed you are, the better prepared you'll be to make decision regarding the security needs of your home and your family.</p>
                        <br>
                        
                       <P> <b>A</b><br><br>

<b>Automated Gate:</b> A portion of a wall or fence that opens and closes using a device. The device controls the entering or exciting through the gate.<br><br>

<b>Automated security:</b> Protection designed to deny unauthorized persons access to property or valuables once alarm is set. Will also alert and/or warn owner and first responders of impending or immediate danger through home alarms which may be silent and/or audible.<br><br>


<b>B</b><br><br>

<b>Battery Back-up:</b> A secondary power source used to provide power in the event the main power source fails. A battery backup is intended for temporary use and to ensure the continual operation duringa power interruption of the main source. Typically, battery backups are used in computers, alarm clocks, and home security systems, where interruption of power would cause a loss of data, service or human harm.<br><br>

<b>Break-in:</b> When someone illegally enters a residence, car, office or other enclosed property using the slightest amount of force (such as pushing open a door) without consent. If there is no intent to commit a crime, i.e. breaking and entering, it may be considered illegal trespassing. If the intent was to commit a crime, it could be considered burglary.
<br><br>
<b>Burglar alarm:</b> A loud warning signal produced by an electronic sound designed to alert the user, neighbors, home security monitoring system and/or police to a specific danger. Sensors are connected to a control unit. Many security sensors are used to indicate the opening of a door or window or detect motion.
<br><br>

<b>C</b>
<br><br>
<b>Carbon monoxide detector (co2 detector):</b> A device that detects the presence of the carbon monoxide (CO) gas in order to prevent carbon monoxide poisoning. CO is a colorless and odorless compound produced by incomplete combustion. The device is designed to measure CO levels over time and sound an alarm before dangerous levels accumulate. This gives people adequate warning to safely ventilate the area or evacuate. Some system-connected detectors also alert a monitoring service that can dispatch emergency services if necessary
<br><br>
<b>CCTV camera:</b> CCTV also known as closed circuit television camera. To use a CCTV you need a camera. The camera captures the picture that will be transmitted. Today's cameras can recognize if a moving object is a walking person, a crawling person or a vehicle. It can also determine the color of the object.
<br><br>
<b>Closed circuit television (CCTV):</b> Is a visual surveillance system used to monitor a variety of environments. CCTV systems may operate continuously or only when required. The picture is viewed or recorded but not broadcast. Often used as part of a home security system. The system may involve a fixed communication link between camera(s) and monitors.
<br><br>
<b>Control Panel:</b> A tool in the operating home system (such as a security system) which access is permitted in order to allow settings to be changed. The security control panel is flat, usually vertical, where the control and/or monitoring are displayed.

<br><br>
<b>D</b>
<br><br>
<b>Deadbolt:</b> Also known as deadlock, a steel latch approximately one inch long, which exits the edge of the door and passes into the door jamb near the keyhole. In this way a deadbolt is distinct from a spring-bolt because a deadbolt cannot be moved to the open position except by turning the cylinder. A deadbolt can either be a single cylinder or a double cylinder. A single cylinder will use a key on one side of the lock and can be operated by a twist of a knob on the other side. A double cylinder lock will use a key on both sides, so there is no knob. A deadbolt is often used as part of a home security system because it makes a door more resistant to entry without the correct key.
<br><br>
<b>Deadlock:</b> Also known as a deadbolt. See deadbolt definition.
<br><br>
<b>Digital video monitoring:</b> Used with a CCTV security system. The monitor allows you to view the output from several cameras at the same time.
<br><br>
<b>Door alarm:</b> A loud warning signal is produced when the alarm is opened, triggered or shut. Once the alarm is triggered a signal is sent to alert the user, home security monitoring center and/or police to a specific danger. The alarm can be wireless. Wireless door alarms may operate with batteries which need to be replaced, or they may be plugged in or wired to the wall.
<br><br>
<b>Door and window contact:</b> A protective device that can send a notification to a compatible home security system when the opening and closure of doors and windows occurs. There are three different styles typically used by many alarm companies; a recessed contact, surface mount contact and roller-ball contact. A recessed contact is hidden in the window track or door jam. A surface mount contact works much the same as a recessed however it is visible when the door or window is closed. A roller boll contact is in the hinge side of a door jam. The spring-leaded ball is pushed in when the door is closed.
<br><br>
<b>Double cylinder lock:</b> Uses a key on both sides of the door. This type of lock is often used with doors that have glass sections on one or both sides because it may deter a potential burglar who could gain access by breaking the glass. One often cited drawback of a double cylinder lock is that in the event of a fire, it could delay or prevent occupants from escaping unless the key is used. This drawback could be mitigated by keeping the key in the lock or nearby when occupants are inside the home

<br><br>
<b>E</b>
<br><br>
<b>Electric gate:</b> Can be made from wood or iron and is electrically powered or controlled. The gate is used to create a barrier which can separate public roads form private driveways or to limit access to any place either by vehicle or pedestrians.
<br><br>
<b>Electronic Surveillance:</b> An electronic device, such as a television, digital video recorder or video camera, which is used to monitor activity or other changing information from a distance. Often used to enhance security of property and valuables as well as to detect or prevent crime.
<br><br>
<b>Emergency Phone:</b> A designated phone, usually with its own line, for the purpose of making calls to contact emergency services.
<br><br>
<b>Emergency Response:</b> When an ambulance, police, fire department or other emergency service responder takes the necessary steps to respond to a call for assistance.

<br><br>
<b>F</b>
<br><br>
<b>Fire alarm:</b> Sends out a signal indicating the unwanted detection of fire. The alarm monitors the environmental changes associated with combustion. Once the alarm has been triggered by fire or smoke a loud sound emanates to warn of danger. A home fire alarm system can be part of a total security system which also provides burglary protection in addition to fire protection.
<br><br>
<b>Flood detector:</b> A device designed to detect the presence of water. Some flood detectors will send out an early warning alarm for leaky pipes, faulty water heaters, dishwashers and washing machines. Some detectors are sensitive enough to detect even the smallest amounts of moisture before mold, mildew and serious water damage or even flooding will occur.

<br><br>
<b>G</b>
<br><br>
<b>Glass break sensor:</b> A sensor used to detect if a pane of glass is shattered or broken or a splintering of the wood around the window happens. Glass break detectors can come in either single technology or double technology devices. Single technology listens for the frequency of breaking glass. Double technology devices must hear a 'thump' sound followed by a frequency hit, in that precise order in order to set off the alarm.

<br><br>
<b>H</b>
<br><br>
<b>Heat detectors:</b> A device that responds to changes in ambient temperature. Heat detectors are not meant to replace smoke detectors. They are often placed in rooms where standard smoke detectors are not suitable, such as laundry rooms, garages, and attics.
<br><br>
<b>Home alarm:</b> A warning device that, when triggered is designed to warn of an intruder, fire or smoke. Home alarms often make up a home security system and serve to call attention to and/or send a signal to a monitoring center or first responders such as police, fire department, and ambulance.
<br><br>
<b>Home automation:</b> Refers to anything that gives you remote or automatic control of things around the home. This can be as simple as having lights on a remote or automatic control to having your home electronics and/or home security system pre-wired or retro-fitted into a residential application system. Some systems include automation of outdoor/indoor lights with their home security system. More advanced home automation may include installing advanced controllers or a voice recognition system.
<br><br>
<b>Home Burglary:</b> When an unauthorized person or persons enters a residence, car, office or other enclosed property for the purpose of committing an offence. Burglary itself is defined as entering an enclosed property without authorization and with intent to commit a crime such as, theft, vandalism or physical harm. The nature of the crime does not matter to be considered burglary; rather it is the intent to commit a crime that matters. The type of burglary determines the degree of the crime.

<br><br>
<b>I</b>
<br><br>
<b>Identity Theft:</b> Is the crime of one person stealing the personal and indentifying information such as social security number, credit cards and bank accounts, of another person for fraudulent purposes such as to, gain access to finances, open credit card, receive medical treatment, fame or other benefits.
<br><br>
<b>IP surveillance:</b> IP-based surveillance systems provide a means for many cameras and video encoders to share the same wired or wireless network for communicating data. The term 'IP' refer to a digital video surveillance system that is tied into a network. A digital image is more easily stored and retrieved when compared to an analog video tape.

<br><br>
<b>K</b>
<br><br>
<b>Keypad:</b> A device with a set of buttons usually arranged in a square, similar to the standard telephone touchpad. Each button bears a number with a set of letters underneath the number or other symbol. Should the buttons contain mostly numbers then it may also be called a numeric keypad. A keypad controls the functions of an alarm system.

<br><br>
<b>L</b>
<br><br>
<b>Larceny:</b> The act of taking another person's personal property without authorization. In some states, larceny is divided into grand and petit larceny depending on the value of the property stolen. A lesser charge is referred to as misdemeanor larceny. When the amount stolen is greater, it is referred to as grand larceny.
<br><br>
<b>Light activation:</b> A system senor that is connected to indoor and/or outdoor lights. When the sensor is activated, the lights will illuminate when someone enters a detected zone. Some systems can be turned on with a remote control. Often used as an effective deterrent to prevent crime. Sensors are weather-sealed for outdoor use and can be set so the light will shut off after 6 seconds, 3 minutes or 10 minutes after motion has stopped.

<br><br>
<b>M</b>
<br><br>
<b>Medical alarm:</b> A system that is installed in the home usually as part of a home security system, and includes a receiver and a mobile pendant or button. The alarm system is designed to signal the presence of a hazard requiring urgent attention and/or to summon emergency medical personnel. A medical alarm is designed to provide the user with 24/7 access to a monitoring center.
<br><br>
<b>Medical alert:</b> Is sometimes referred to as a medical alarm (see medical alarm definition). The more common definition is the coding of the patient's medical or dental record to indicate the preexisting presence of a serious medical condition. This alerts medical staff that treatment planning needs to take part before treatment of any kind is initiated. The alert is often identified by a red warning label containing a notation as to the exact nature of the condition and is usually placed on the medical record or chart.
<br><br>
<b>Mobile home security:</b> Is the ability of a homeowner to have mobile and remote access to the home security system.
<br><br>
<b>Motion sensor light:</b> A light that has a sensor which detects movement. When movement is detected the light will illuminate. Motion sensor lights can be used indoors or outdoors and are often used for home security.

<br><br>
<b>O</b>
<br><br>
<b>Outdoor lighting:</b> A security lighting system allows residents to use outdoor lights as a deterrent. When lights are properly placed, they can illuminate exterior areas where a criminal may hide or use to access a residence. Homeowners may benefit from a lighting security system that can detect and recognize movement in an otherwise overlooked and dark surrounding. When positioning security lights, it is important to control light glare. Bright lights can create dark shadows in the negative space. This can make it harder for eyes to adjust and pick out potential intruders hiding in the dark spots.
<br><br>
<b>Outdoor surveillance:</b> Outdoor security cameras are used for the surveillance of the exterior of the building, home or office. Outdoor security cameras can be wired or wireless. Wireless security cameras are very convenient to use because they do not have cables. Wireless security cameras are weather proof.

<br><br>
<b>P</b>
<br><br>
<b>Panic button:</b> An emergency button that, when pressed, triggers an alarm system as well as sends a signal to authorities.
<br><br>
<b>Panic room:</b> A fortified room located in a residence and/or building designed to provide a safe place for the inhabitants to hide in the event of a break-in, home invasion, or other threat. A panic room may also have a separate emergency telephone line.
<br><br>
<b>Personal alarm:</b> Is a discrete, hand-held electronic device that will emit a loud siren-like alarming sound when activated. It's primarily used to ward off an attack by an assailant and attract attention.
<br><br>
<b>Personal property:</b> Property that is private and moveable and whose ownership belongs to an individual. This includes everything that is tangible, from office furniture to cars and jewelry, promissory notes, to clothing. Real estate is not considered personal property.
<br><br>
<b>Personal safety:</b> The freedom from fear or worry regarding a person's physical and mental well-being. Being aware of one's surrounding and avoiding situations, and conditions which may be potentially harmful or dangerous.
<br><br>
<b>Phone activation:</b> To activate or deactivate your home security system over the telephone when you are away from home.

<br><br>
<b>R</b>
<br><br>
<b>Remote home security:</b> Is a way for residents to quickly and easily activate their home security alarm system from a distance with the simple push of a button. Some home surveillance security packages provide constant remote monitoring access via the web by a professional security company. Remote home monitoring can be a phone call made directly to your cell phone that alerts you when an alarm is triggered. Advanced home security systems are connected to a professional monitoring service that receives the alerts and will then contact the proper authorities on your behalf.

<br><br>
<b>S</b>
<br><br>
<b>Safewatch cellguard:</b> A cellular product offered by ADT that enables your home security system to communicate with ADT's customer monitoring centers. Provides protection if your home telephone service is disrupted due to fallen lines, storms or construction.
<br><br>
<b>Security camera:</b> Capture images by transforming the visible scene observed by a camera lens into an electric signal to be processed. Often used as a means to deters criminal behavior.
<br><br>
<b>Security monitoring:</b> If you have activated your home security system and any of the home security sensors or detectors are set off, an emergency signal is transmitted either via phone lines, the internet or cellular towers to the central station. Upon receiving the signal, a trained professional will notify the appropriate authority.
<br><br>
<b>Security risk assessment:</b> The process of identifying all possible risks, their likelihood of occurrence and their impact. The results of the assessment are used to identify ways to reduce risk and improve safety.
<br><br>
<b>Security system:</b> an electrical device that sets off an alarm and notifies, residents, authorities and security center once a sensor is triggered such as in the case of a possible burglar, intrusion, fire, or other safety monitoring.
<br><br>
<b>Smoke detector:</b> A device used to detect smoke, typically as an indicator of fire. Household detectors also known as smoke alarms, generally sound an audible and/or visual alarm. Smoke detectors are usually housed in a disk-shaped plastic enclosure about 6 inches in diameter and 1 inch thick and are often powered by a disposable battery. The shape of a smoke detector can vary by manufacturer.
<br><br>
<b>Spy camera:</b> A hidden camera can be a still or video camera used to film people without their knowledge.
<br><br>
<b>Surveillance system:</b> Electronic devices used to maintain close observation of areas from a distance and without being seen.

<br><br>
<b>T</b>
<br><br>
<b>Temperature sensor:</b> A device gathers data concerning the temperature from a source and converts it to a form that can be understood by an observer. Temperature sensors come in many different forms and are used for a wide variety of purposes, from simple home use to extremely accurate and precise scientific use
<br><br>
<b>Tempered glass:</b> Is a type of glass that has been treated with heat in the manufacturing. This creates a product that is much stronger and can withstand abnormal force or pressure. The result of this treated glass is that when it does break, will not be into sharp pieces. Tempered glass is found in doors and in windows that are located near doors.
<br><br>
<b>Theft protection:</b> A way to protect against or reduce the likelihood of having personal property taken without authorization. An insurance policy which provides financial coverage and reimbursement in the event personal property is stolen.
<br><br>
<b>Two-way keypad:</b> A device that will let a person arm and disarm an alarm system from inside or outside a residence. It also activates an audio connection to a monitoring center in the event an alarm is triggered.


<br><br>
<b>W</b>
<br><br>
<b>Window lock:</b> A lock that is attached to the sashes of a double hung window that can fit both in the shut position. There are devices available which are designed to make prying open a window and/or removing a sliding glass door from its track more difficult. The right locking device to choose will depend on whether you need to secure a sliding window or door or a double-hung window.
<br><br>
<b>Wireless home security:</b> A home security system that protects a home from theft, fire and other emergencies without the invasive installation of running wires. It does however require the installation of sensor boxes around doors and windows. Wireless systems have fewer components, which could mean less potential maintenance.

<br><br>
<b>X</b>
<br><br>
<b>x10 system:</b> A two-way switch, that can be hard wired or wireless. The technology provides the ability to control a lamp without running any wires.

