						<h3><a href="#" style="color:#C93D3D;">Shine a light on the problem</a></h3><br>
					
                        <p>Strategically place outdoor lights can deter potential intruders. Criminals do not want to be identified which is why they target property that offers them the most protection from neighbors and passersby. Here are 3 types of lighting to consider each with its own advantages and disadvantages:
</p><br>
<p><b>All-Night Lighting:</b> Covers your property with constant lighting, making it difficult for an intruder to enter without being seen. This type of lighting tends to be the most popular.</p><br>
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span><b>Pros:</b> It’s convenient. Can be set to turn on at dusk and off at dawn with a light-sensitive photocell.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span><b>Cons:</b> This type of lighting uses the most electricity. It can add a significant Increase to your utility bill.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span><b>Tip:</b> Keep energy consumption down by using solar energy lighting.</a></li>
                            
                           
                           
                        </ul>
                        
                         <br>
                         
                        
						<p><b>Motion-Activated Lighting:</b> Turns on when someone or something passes by a motion sensor.</p>
                        <br>
                        <ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span><b>Pros:</b>  It can be quite frightening for a trespasser when lights suddenly turn on, one reason this type of light is so effective and popular. This type of lighting saves electricity, as it is only activated when needed.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span><b>Cons:</b>  Light needs to be in the right position to turn on when you want it.</a></li>
                          
                           
                        </ul>
                         
                          <br>
                         
                        
						<p><b>Hi/Low Combination Lighting:</b> Uses a low intensity light throughout the night. When someone passes by, the motion sensor causes the lights to get brighter and brighter.</p>
                        <br>
                        <ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span><b>Pros:</b>  Saves some money on electricity while providing complete illumination when needed.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span><b>Cons:</b>  None</a></li>
                          
                           
                        </ul>
                       <br>
                       <h3><a href="#" style="color:#C93D3D;">Landscape Security</a></h3><br>
					
                        <p>Your landscape can have a major role in either increase or decrease the safety of your exterior. Take a survey of your front and back yard. Notice when shrubbery is overgrown or can provide concealment for unwanted intruders and cut the growth back. Use strategically placed shrubbery (such as a thorny olive bush) to deter trespassers. Add the outdoor lighting of your choice that provides your lawn with beauty while providing your home with security.
</p><br>
                       
                     
