                    <h3><a href="#" style="color:#C93D3D;">Q: What is VoIP/Digital Phone Service?</a></h3><br>
						<p><b>A:</b> Voice over Internet Protocol (also known as VoIP) and Digital Phone are forms of phone service provided by cable companies, telecommunications companies and, in some cases, independent providers. Rather than use telephone lines when an alarm from home security systems is triggered, these companies use the Internet connection that is already present in your home to provide home phone service. Although these technologies provide a form of telephone service, there may be differences in their range of features and reliability*.</p>
                        <br>
                        
                          <h3><a href="#" style="color:#C93D3D;">Q: Will switching to VoIP/Digital Phone service affect my ADT security system?</a></h3><br>
						<p><b>A:</b> It could, depending on which provider service you use. For this reason we recommend that before you consider a change in telephone service you contact us ADT at 1-800-ADT-ASAP. We can tell you which VoIP/Digital Phone service providers in your local area meet the necessary characteristics to become a Qualified Managed Facility Voice Network (MFVN). Those providers that qualify are included in ADT’s policy and are rated as an acceptable primary method of transmitting alarm signal to ADT’s monitoring centers.</p>
                        <br>
                        
                         <h3><a href="#" style="color:#C93D3D;">Q: How does my ADT security system work?</a></h3><br>
						<p><b>A:</b> Most alarm system control panels, including ADT’s, communicate with customer monitoring centers through a telephone line. During an alarm event, the control panel seizes control of this telephone line (referred to as "line seizure"), which should allow an alarm signal to be sent even if a telephone receiver in the home is off the hook. 
<br>
With some VoIP/Digital Phone services, ADT’s ability to receive an alarm signal may be impacted if there is a power outage at your home or if the VoIP/Digital Phone service is temporarily unavailable due to scheduled or unscheduled network outages. These risks vary widely among the VoIP/Digital Phone technologies used today.</p>
                        <br>
                        
                        
						<h3><a href="#" style="color:#C93D3D;">Q: What is ADT’s policy on VoIP/Digital Phone Services?</a></h3><br>
						<p><b>A:</b> ADT’s policy requires that VoIP/Digital Phone services that at least one of the following system be used as the primary method for transmitting alarm signals to ADT’s Customer Monitoring Centers:</p>
                        <br>
                        
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Traditional Telephone Service</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Cellular Communications Service such as Safewatch® CellGuard®</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>A phone service provided by a Qualified "Managed Facility Voice Network (MFVN)" provider.**</a></li>
                           
                        </ul>
                        <br>
                        <p>Other methods of communication, beyond the three listed above, may not be used as the primary means for transmitting an alarm signal to ADT’s monitoring centers. In addition, regardless of the type of telephone service that you have, ADT always recommends that customers use an additional back-up method of communication to connect their alarm system to ADT’s monitoring center, such as ADT Safewatch® CellGuard®.</p>
                        
                         
                         <br>
                        <h3><a href="#" style="color:#C93D3D;">Q: What is Safewatch® CellGuard®?</a></h3><br>
						<p><b>A:</b> Safewatch® CellGuard® is a cellular product offered by ADT that enables your home security system to communicate with ADT’s customer monitoring centers.
<br>
It has become a popular option in home security systems because it helps provide protection should your home telephone service be disrupted due to fallen lines, storms or construction.***</p>
                        
                         <br>
                         
                         <h3><a href="#" style="color:#C93D3D;">Q: What is a Qualified "Managed Facility Voice Network (MFVN)"?</a></h3><br>
						<p><b>A:</b> A Qualified "Managed Facility Voice Network (MFVN)"includes the following:</p>
                        <br>
                        
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Has a physical facilities network which is managed and maintained (directly or indirectly) by the service provider. Can ensure service quality from the service subscriber location to the PSTN or other MFVN peer network.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Utilizes similar signaling and related protocols as the PSTN with respect to dialing, dial plan, call completion, carriage of alarm signals and protocols, and loop voltage treatment.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Provides real-time transmission of voice signals, carrying alarm formats unchanged.</a></li>
                            	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Provides professional installation that preserves primary line seizure for alarm signal transmission.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Has major and minor disaster recovery plans to address both individual customer outages and widespread events such as tornados, ice storms and other natural disasters. This includes specific network power restoration procedures that are comparable to those of traditional landline telephone services in the same geographic region.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Has informed ADT that its network meets the characteristics of a MFVN.</a></li>
                           
                        </ul>
                        <br>
                        <p>Contact us ADT at 1-800-ADT-ASAP to find out which VOIP/Digital Phone service provider network meet the necessary characteristics to become a Qualified Managed Facility Voice Network (MFVN).</p>
                         <br>
                         
                         <h3><a href="#" style="color:#C93D3D;">Q: Is the phone service I want included in ADT’s VoIP/Digital Phone Policy?</a></h3><br>
						<p><b>A:</b> ADT keeps a database of the phone services that are in and out of ADT’s policy for the transmission of alarm signals. This database changes over time as more phone service providers inform ADT that they meet the definition of a Qualified Managed Facility Voice Network. ADT customers can call one of our ADT customer monitoring centers at 1-800-ADT-ASAP to ask which local VOIP/Digital Phone service provider networks meet the necessary characteristics to become a Qualified Managed Facility Voice Network (MFVN).</p>
