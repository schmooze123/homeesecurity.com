						<h3><a href="#" style="color:#C93D3D;">3 amazing benefits of complete home automation:</a></h3><br>
                        <h3><a href="#" style="color:#337ab7;">#1 Convenience</a></h3><br>
						<p>Life, and sometimes traffic can make it difficult to be home the moment the kids come in the door or when the repair person shows up. And since you can't be in two places at the same time, having the convenience of accessing your home systems remotely can be a real time saver. Turn off all the lights in your home without having to get out of bed. Use your cell phone or portal device to turn your wireless home security alarm system on/off or use voice recognition to control appliances around the house.</p>
                        <br>
                        <h3><a href="#" style="color:#337ab7;">#2 Energy Efficiency & Savings</a></h3><br>
						<p>Many home automation products, also known as "smart" products, help you manage your home's energy consumption. For example, automate your thermostat to adjust settings throughout the day based around the times someone is home or the house is empty.
<br>
Some "smart" devices can be synced up to your appliances with real-time energy information. This helps your home appliances know the most cost-effective times to operate. For example, your dishwasher may run at 2am because this is the best time for the lowest price.</p>
                        <br>
                         <h3><a href="#" style="color:#337ab7;">#3 Wireless Home Security</a></h3><br>
						<p>Complete home automation systems have many security benefits. As mentioned, they allow you to check in on your home from a remote location, giving you true peace of mind. Some systems will let you interact with your home security system, providing you with the ability to arm and disarm your home remotely. Some complete home automation systems will alert you by phone, text or email if there is any unusual movements within your home.
<br>
Home automation products provide convenience and save you time and effort performing household tasks. They help conserve energy and save money by reducing your home's energy consumption. Maybe and perhaps most importantly, complete home automation lets you customize your home to fit your family's unique needs and lifestyle.</p>
