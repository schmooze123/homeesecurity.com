						<h3><a href="#" style="color:#C93D3D;">What you need to know</a></h3><br>
					
                        <p>Carbon monoxide, also known as the silent killer, is an odorless and colorless gas produced by stoves, lanterns, burning charcoal and wood, as well as gas ranges and heating systems. Many people mistake the symptoms of CO poisoning for coming down with the flu.
</p><br>
<p>The symptoms of carbon monoxide include:</p><br>
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Headache</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Nausea</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Dizziness</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Vomiting</a></li>
                           
                           <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Confusion</a></li>
                        </ul>
                        
                         <br>
                        <h3><a href="#" style="color:#C93D3D;">What’s that sound?</a></h3><br>
						<p>It’s safe to say that most people are more familiar with the sound of their smoke detector or fire alarm then with the sound of their carbon monoxide alarm. For this reason it is important to know that when your CO detector goes off it will sound like intermittent beeping.The sound will be much like the one your smoke detector makes when the battery needs to be changed. When this alarm sounds (intermittent beeps) it means this gas has been leaking unbeknownst to you and it has reached a dangerous level.</p>
                        <br>
                         <h3><a href="#" style="color:#C93D3D;">What to do when you hear that sound.</a></h3><br>
						<p>The first thing –DO NOT ignore the beeping of the alarm.
<br>
If you or others in your household are experiencing any of the symptoms mentioned previously follow these directions:</p>
         <br>                
                      <ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Call 9-1-1 immediately.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Go to a hospital immediately.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>DO NOT re-enter the home until emergency services has indicated it is safe to return.</a></li>
                            <p>If no one is showing signs or symptoms of carbon monoxide poisoning follow these directions:</p>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Open all windows to get air ventilation.</a></li>
                           
                           <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Turn off any appliances such as your gas-fired furnace or a running generator.</a></li>
                           
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>After the home has gotten ventilation, reset the carbon monoxide detectors.</a></li>
                           
                           <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>If the detectors do not sound again, call a qualified technician to inspect and repair any problem.</a></li>
