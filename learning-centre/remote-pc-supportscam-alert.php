						<h3><a href="#" style="color:#C93D3D;">Remote PC support makes sense</a></h3><br>
						<p>It makes sense that people would at some point have computer issues. Then, just as if the universe hears your cries of frustration, you get a call or email from someone posing as tech support from a reputable IT vendor. PC World calls these scams, 'social engineering' or 'trust' scams. The scams seem to work in a few different ways.</p>
                        <br>
                        <h3><a href="../solutions/24hr-monitoring.php" style="color:#C93D3D;">Remote PC scams you should know about</a></h3><br>
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>A scammer calls you posing as remote access service technician. The caller claims to be calling on behalf of the reputable IT company or vendor. The caller then tries to convince you of a real or nonexistent problem with their software and offers to fix it for you. To fix the problem, the caller tries to convince you to use or purchase their security software and malware removal services.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>In this scam you are contacted by the scammer posing to be from tech support and offering to help you with your computer problem via remote pc service. The scammer tells you in order to press “Windows Key + R” which opens the “Run” dialogue.” This allows the scammer to download spyware which may be used to steal your personal information, i.e. bank numbers, pass codes, etc.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>You receive an email from a source claiming to be from a reputable IT vendor. The email asks you to visit a website (which looks official) to download the necessary software or the email provides a link for you to click to obtain the software. In reality the attachment or link contains malware.</a></li>
                           
                          
                        </ul>
                        
                         <br>
                       
						<p>It may not be clear how the scammer gets your phone number, email address or any of your contact information, but it does demonstrate why there is a need to develop a relationship with a trust IT tech. In the meantime, if anyone emails or calls you with a ruse that your PC needs attention –just hang up or delete the email.</p>
                        
