						<h3><a href="#" style="color:#C93D3D;">For homeowners, some of the common causes are:</a></h3><br>
						<br>
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Using incorrect keypad codes.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Failing to train authorized users.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Failure to secure doors and windows once the alarm is turned on.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Wandering pets.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Re-entering the home just after leaving without disarming (assuming the exit delay is long enough to compensate).</a></li>
                            
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Objects hanging by or around motion detectors.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Weak system batteries.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Faulty equipment.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Acts of nature (strong winds, electrical storms, etc.).</a></li>
                        </ul>
                        
                         <br>
                        
						<p>False alarms due to faulty equipment or acts of nature are rare. The single largest cause of false alarms is human error.</p>
                        
                         <br>
                       
                       
                       <h3><a href="#" style="color:#C93D3D;">Once the causes are identified, some basic steps should help reduce false alarms:</a></h3><br>
						<br>
						<ul class="list">
                        	<li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Properly train all users (e.g., babysitters, relatives, children, visitors, etc.).</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Secure doors and windows before turning on alarm.</a></li>
                  
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Inform the monitoring center of new pass codes and arming codes, and new or removed authorized users.</a></li>
                            <li><a href="#"><span class="glyphicon glyphicon-bookmark" aria-hidden="true"></span>Service and maintain the system (including batteries) properly</a></li>
                            
                           
                        </ul>
                        
                         <br>
                        
						<p>If there is a question as to whether or not the system is working properly, immediately contact the security provider to check the status of the system and devices.</p>
                        
